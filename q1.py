# Q1. Faça um algoritmo que lê dois números,
#  e verifica se os dois números são iguais.
#   Se forem iguais, escrever "São iguais", 
#   se não, escrever "Não são iguais".
n1 = int(input('digite o primeiro número\n'))
n2 = int(input('digite o segundo número\n'))
if(n1==n2):
    print('os números são iguais')
else:
    print('os números são diferentes')
