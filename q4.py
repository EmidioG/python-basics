# Q4. Faça um algoritmo que lê dois números, e verifica 
# se o primeiro número é igual ao segundo número. Se 
# forem iguais, escrever "Números iguais". Se não, 
# escrever "Números diferentes".


n1 = int(input('digite o primeiro número\n'))
n2 = int(input('digite o segundo número\n'))
